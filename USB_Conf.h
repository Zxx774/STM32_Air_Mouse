#ifndef __USB_CONF_H
#define __USB_CONF_H

// =============================================================
// 1. 端点数量定义
// =============================================================
#define EP_NUM              (2)   // 我们使用 EP0(控制) 和 EP1(鼠标数据)

// =============================================================
// 2. 缓冲区地址分配 (PMA)
// =============================================================
// BTABLE 是缓冲区描述表的起始地址，放在 PMA 的开头 0x00
#define BTABLE_ADDRESS      (0x00)

// --- 端点 0 (控制传输) ---
// Rx (接收) 缓冲区地址偏移，预留64字节
#define ENDP0_RXADDR        (0x40)
// Tx (发送) 缓冲区地址偏移，预留64字节
#define ENDP0_TXADDR        (0x80)

// --- 端点 1 (鼠标数据发送) ---
// Tx (发送) 缓冲区地址偏移，紧接在 EP0 后面
// 0x80 + 64字节(0x40) = 0xC0
#define ENDP1_TXADDR        (0xC0) 

// =============================================================
// 3. 中断屏蔽位设置
// =============================================================
// 开启复位(RESET), 挂起(SUSP), 唤醒(WKUP), 控制传输(CTR)
#define IMR_MSK (CNTR_CTRM | CNTR_WKUPM | CNTR_SUSPM | CNTR_ERRM | CNTR_SOFM | CNTR_ESOFM | CNTR_RESETM)

#endif /* __USB_CONF_H */
